    private void AbrirArquivoHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirArquivoHandler
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        int opcao = chooser.showOpenDialog(eu);
        if (opcao == JFileChooser.APPROVE_OPTION) {
            jBLerArquivo.setEnabled(true);
            nomeArq = chooser.getSelectedFile().getName();
            File currentDirectory = chooser.getCurrentDirectory();
            nomeArq = currentDirectory + File.separator + nomeArq;
            lNomeAquivo.setText(nomeArq);
        } else if (opcao == JFileChooser.CANCEL_OPTION) {
            lNomeAquivo.setText("Opção Candelada");
        }
    }//GEN-LAST:event_AbrirArquivoHandler

    private void jBLerArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLerArquivoActionPerformed
        // TODO add your handling code here:
        if (!nomeArq.equals("")) {

            jBLerArquivo.setEnabled(false);

            List<String> arquivoLido = leitorArquivo(nomeArq);
            System.out.println("---Lendo arquivo-----\n" + arquivoLido);
            carregarTextoTela(arquivoLido);

        } else {
            JOptionPane.showMessageDialog(null, "Arquivo não selecionado", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBLerArquivoActionPerformed

    public void carregarTextoTela(List<String> arquivoLido) {
        String linha = arquivoLido.get(0).trim();

        String cnpjExtraido = linha.substring(linha.indexOf(";") + 1, linha.lastIndexOf(";"));
        jTextCnpj.setText(cnpjExtraido);

        String empresa = linha.substring(linha.lastIndexOf(";") + 1, linha.length());
        jTextEmpresa.setText(empresa);
        //
        String linha2 = arquivoLido.get(1).replace(";", " ");

        String pedidoNum = linha2.substring(2, linha2.indexOf(" ", 2));
        jTextPedido.setText(pedidoNum);

        String valor = linha2.substring(pedidoNum.length() + 3, linha2.indexOf("A"));
        jTextValorPedido.setText(valor);

        String condicao = linha2.substring((valor.length() + pedidoNum.length()) + 2, linha2.length() - 11).trim();
        jTextCondicaoPgto.setText(condicao);

        String data = linha2.substring(linha2.lastIndexOf(" "), linha2.length() - 1).trim();
        jTextDataEnvio.setText(data);

        limparTabela();
        DefaultTableModel tabela = (DefaultTableModel) tabelaModelo.getModel();
        for (int i = 2; i < arquivoLido.size(); i++) {
            String linha3 = arquivoLido.get(i).replace(";", " ");
            String codigoItem = linha3.substring(2, 7);
            String descricao = linha3.substring(codigoItem.length() + 2, codigoItem.length() + 13).trim();
            int tamanho = linha3.lastIndexOf(" ");
            String qtde = linha3.substring((codigoItem.length() + 1) + (descricao.length() + 2), tamanho).trim();
            String subtotal = linha3.substring(linha3.length() - 6, linha3.length()).trim();

            tabela.addRow(new Object[]{codigoItem, descricao, qtde, subtotal});
        }
    }

