    public List<String> leitorArquivo(String nameFile) {
        FileReader leitorArquivo = null;
        BufferedReader buferizador = null;
        List<String> listandoString = new ArrayList<>();
        try {
            leitorArquivo = new FileReader(nameFile);
            buferizador = new BufferedReader(leitorArquivo);
            String texto = buferizador.readLine();
            while (texto != null) {
                listandoString.add(texto + "\n");
                texto = buferizador.readLine();
            }
        } catch (IOException ex) {
            Logger.getLogger(TelaVenda.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (buferizador != null && leitorArquivo != null) {
                    buferizador.close();
                    leitorArquivo.close();
                }
            } catch (IOException ex) {
                Logger.getLogger(TelaVenda.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return listandoString;
    }

    public void gravarArquivo() {
        FileWriter gravador = null;
        BufferedWriter buferizador = null;
        String texto = pegarTextoTela();
        try {
            gravador = new FileWriter(nomeArq);
            buferizador = new BufferedWriter(gravador);
            buferizador.write(texto);
//            buferizador.newLine();
            buferizador.flush();
            System.out.println("Arquivo salvo: " + texto);
            JOptionPane.showMessageDialog(null, "Dados Alterados!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(TelaVenda.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (buferizador != null && gravador != null) {
                    gravador.close();
                    buferizador.close();
                }
            } catch (IOException ex) {
                System.out.println("Erro ao fechar buffer");
                ex.printStackTrace();
            }
        }
    }

    public String pegarTextoTela() {

        String cnpj = jTextCnpj.getText().trim();
        String empresa = jTextEmpresa.getText().trim();
        String pedido = jTextPedido.getText().trim();
        String valor = jTextValorPedido.getText().trim();
        String condicao = jTextCondicaoPgto.getText().trim();
        String data = jTextDataEnvio.getText().trim();

        String tabela = pegarTextoTabela();

        return "1;" + cnpj + ";" + empresa
                + "\n2;" + pedido.trim() + ";" + valor.trim() + ";" + condicao.trim() + ";" + data.trim()
                + "\n" + tabela;
    }

    public String pegarTextoTabela() {
        int totalLinhas = tabelaModelo.getRowCount();
        DefaultTableModel tabela = (DefaultTableModel) tabelaModelo.getModel();
        String textoLinha = "";
        int contador = 0;

        System.out.println(totalLinhas);
        //textoLinha = "3;" + String.valueOf(tabela.getValueAt(0, 0));
        //System.out.println(textoLinha);

        while (contador < totalLinhas) {
            textoLinha += "3;" + String.valueOf(tabela.getValueAt(contador, 0))
                    + ";" + String.valueOf(tabela.getValueAt(contador, 1))
                    + ";" + String.valueOf(tabela.getValueAt(contador, 2))
                    + ";" + String.valueOf(tabela.getValueAt(contador, 3)) +"\n";
            contador++;
        }
        System.out.println("tabela :" + textoLinha);
        return textoLinha;
    }

