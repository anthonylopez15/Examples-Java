    public void limparTabela() {
        int linha = tabelaModelo.getRowCount();
        DefaultTableModel tabela = (DefaultTableModel) tabelaModelo.getModel();
        for (int i = linha - 1; i >= 0; i--) {
            tabela.removeRow(i);
        }
    }

